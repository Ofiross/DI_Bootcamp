# Exercise 2 : What Is The Season ?
user_month = int(input('Please choose a month from 1-12'))
if user_month == (3 or 4 or 5):
    print('Your month is in spring time')
elif user_month == (6 or 7 or 7):
    print('Your month is in Summer time')
elif user_month == (9 or 10 or 11):
    print('Your month is in Autumn time')
elif user_month == (10 or 1 or 2):
    print('Your month is in Winter time')
